public static String weekday(int y){
        if(y==1){
            return "Monday";
        }
        else if(y==2){
            return "Tuesday";
        }
        else if(y==3){
            return "Wednesday";
        }
        else if(y==4){
            return "Thursday";
        }
        else if(y==5){
            return "Friday";
        }
        else if(y==6){
            return "Saturday";
        }
        else if(y==7){
            return "Sunday";
        }
        else{
            return "Are you stupid??? HAHAHAHAHA~";
        }
    }
